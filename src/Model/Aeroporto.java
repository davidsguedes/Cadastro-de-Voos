public class Aeroporto {
    
    private int idAeroporto;
    private String aercod;
    private String aerSigla;
    private String aerlocalizacao;

    /**
     * @return the idAeroporto
     */
    public int getIdAeroporto() {
        return idAeroporto;
    }

    /**
     * @param idAeroporto the idAeroporto to set
     */
    public void setIdAeroporto(int idAeroporto) {
        this.idAeroporto = idAeroporto;
    }

    /**
     * @return the aerSigla
     */
    public String getAerSigla() {
        return aerSigla;
    }

    /**
     * @param aerSigla the aerSigla to set
     */
    public void setAerSigla(String aerSigla) {
        this.aerSigla = aerSigla;
    }

    /**
     * @return the aercod
     */
    public String getAercod() {
        return aercod;
    }

    /**
     * @param aercod the aercod to set
     */
    public void setAercod(String aercod) {
        this.aercod = aercod;
    }

    /**
     * @return the aerlocalizacao
     */
    public String getAerlocalizacao() {
        return aerlocalizacao;
    }

    /**
     * @param aerlocalizacao the aerlocalizacao to set
     */
    public void setAerlocalizacao(String aerlocalizacao) {
        this.aerlocalizacao = aerlocalizacao;
    }

    
    
    
    
    
    
}
